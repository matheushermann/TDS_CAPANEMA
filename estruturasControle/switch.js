let dia = 7; // Dia da semana
switch (dia){
    case 1:
        console.log("Domingo")
        break;
    case 2:
        console.log("Segunada-feira");
        break;
        
    case 3:
        console.log("terça-feira");
        break;

    case 4:
        console.log("quarta-feira");
        break;

    case 5:
        console.log("quinta-feira");
        break;

    case 6:
        console.log("sexta-feira");
        break;

    case 7:
        console.log("Sabado");
        break;
    default:
        console.log("numero invalido")

}
